#include <stdio.h>

int main(void) {
    int a = 20;
    int b = 6;

    printf("a = %d, b = %d\n", a, b);
    printf("Suma = %d\n", a + b);
    printf("Resta = %d\n", a - b);
    printf("Multiplicacion = %d\n", a * b);
    printf("Division entera = %d\n", a / b);
    printf("Residuo = %d\n", a % b);

    return 0;
}